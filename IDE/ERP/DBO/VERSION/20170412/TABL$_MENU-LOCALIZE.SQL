EXECUTE BLOCK AS
  DECLARE VARIABLE P_MENU_ID   TYPE OF COLUMN TABL$_MENU.ID;
  DECLARE VARIABLE P_MENU_NAME TYPE OF COLUMN TABL$_MENU.NAME;
BEGIN
  FOR
    SELECT M.ID, M.NAME
    FROM   TABL$_MENU M
    INTO   :P_MENU_ID, :P_MENU_NAME
  DO
    BEGIN
    UPDATE OR INSERT INTO TABL$_LOCAL(COUNTRY_ID, OBJ_ID, COL_ID, REC_ID, NAME)
      VALUES(1000054, 'TABL$_MENU', 'NAME', :P_MENU_ID, :P_MENU_NAME)
      MATCHING(COUNTRY_ID, OBJ_ID, COL_ID, REC_ID);
    UPDATE OR INSERT INTO TABL$_LOCAL(COUNTRY_ID, OBJ_ID, COL_ID, REC_ID, NAME)
      VALUES(1000071, 'TABL$_MENU', 'NAME', :P_MENU_ID, :P_MENU_NAME)
      MATCHING(COUNTRY_ID, OBJ_ID, COL_ID, REC_ID);
    END
END
