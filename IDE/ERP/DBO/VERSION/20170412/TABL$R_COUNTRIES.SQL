EXECUTE BLOCK AS
  DECLARE VARIABLE P_SQL TYPE OF COLUMN TABL$R_COUNTRIES.NAME;
BEGIN
  IF(NOT(EXISTS(
      SELECT RF.RDB$FIELD_ID
      FROM   RDB$RELATION_FIELDS RF
      WHERE  (TRIM(RF.RDB$RELATION_NAME) = 'TABL$R_COUNTRIES')
        AND  (TRIM(RF.RDB$FIELD_NAME)    = 'FLAG_INTRF')
  )))THEN
    BEGIN
    P_SQL ='ALTER TABLE TABL$R_COUNTRIES ADD FLAG_INTRF DOMN$BOOLEAN;';
    EXECUTE STATEMENT :P_SQL;
    END
  IF(NOT(EXISTS(
      SELECT RF.RDB$FIELD_ID
      FROM   RDB$RELATION_FIELDS RF
      WHERE  (TRIM(RF.RDB$RELATION_NAME) = 'TABL$R_COUNTRIES')
        AND  (TRIM(RF.RDB$FIELD_NAME)    = 'VALUTE_SYMBOL')
  )))THEN
    BEGIN
    P_SQL ='ALTER TABLE TABL$R_COUNTRIES ADD VALUTE_SYMBOL DOMN$PSTRING_05;';
    EXECUTE STATEMENT :P_SQL;
    END
END
